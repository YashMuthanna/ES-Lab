	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN 
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R5,=DST
	LDR R1,=0x000000FE
	AND R3,R1,0x0000000F
	AND R4,R1,0x000000F0
	LSR R4,#4
	CMP R3,#9
	BLE NEXT
	ADD R3,#7
NEXT ADD R3,0x30
	STRB R3,[R5],#1
	CMP R4,#9
	BLE NXT
	ADD R4,#7
NXT ADD R4,0x30
	STRB R4,[R5]
STOP B STOP
	AREA mydata, DATA, READWRITE
DST DCD 0
	END
