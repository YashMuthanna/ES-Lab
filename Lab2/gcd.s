	AREA reset, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD	Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	EXPORT Reset_Handler
	ENTRY
Reset_Handler
	LDR R0, =src1
	LDR R1, [R0],#4
	LDR R2, [R0]
	ldr R3, =DST
UP 	CMP R1, R2
	BEQ DOWN ;Z=1 WHEN R1, R2
	BCC DOWN1
	SUB R1, R2
	B DOWN2
DOWN1	SUB R2, R1
	B DOWN2
DOWN2  BNE UP
DOWN STR R1, [R3]
	
	
STOP B STOP
src1 DCD 0x00000008, 0x0000000C

	AREA mydata, DATA, READWRITE
DST DCD 0,0
	END

